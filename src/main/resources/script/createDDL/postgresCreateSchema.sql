CREATE TABLE locuslink.item (
		ITEM_ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY ( START WITH 1 INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 NO CYCLE CACHE 20),
		ITEM_TYPE    VARCHAR(10) NOT NULL,
		ADD_BY  VARCHAR(20) NOT NULL,
		ADD_TIMESTAMP TIMESTAMP NOT NULL,
	    UPDATE_BY  VARCHAR(20) NOT NULL,
		UPDATE_TIMESTAMP TIMESTAMP,
			
		PRIMARY KEY	(ITEM_ID)
	);
commit;



